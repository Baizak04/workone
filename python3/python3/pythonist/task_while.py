n = randint(0, 100)

while True:
    b = input()
    if b == 'exit':
        print('Игра окончена')
        break
    if int(b) > n:
        print('число n меньше')
    else:
        print('число n больше')
        
    if int(b) == n:
        print(f'Вы угадали! Загаданное число = {n}')
        break